namespace HASS.Agent.Enums
{
    /// <summary>
    /// Specifies how the application theme is determined
    /// </summary>
    public enum ThemeMode
    {
        /// <summary>
        /// Follow the Windows system theme setting
        /// </summary>
        System = 0,

        /// <summary>
        /// Always use light theme
        /// </summary>
        Light = 1,

        /// <summary>
        /// Always use dark theme
        /// </summary>
        Dark = 2
    }
}
